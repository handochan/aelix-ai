    # === Utility helpers (Pi parity) ==========================================

    async def wait_for_idle(
        self, timeout_ms: int | None = None
    ) -> None:
        """Pi parity: ``rpc-client.ts:waitForIdle`` (60 s default).

        Resolves when an ``agent_end`` event arrives. Raises
        :class:`asyncio.TimeoutError` on timeout.
        """

        timeout_s = (timeout_ms or self.DEFAULT_WAIT_FOR_IDLE_MS) / 1000.0
        idle = asyncio.get_running_loop().create_future()

        def _listener(event: dict[str, Any]) -> None:
            if event.get("type") == "agent_end" and not idle.done():
                idle.set_result(None)

        unsubscribe = self.on_event(_listener)
        try:
            await asyncio.wait_for(idle, timeout=timeout_s)
        finally:
            unsubscribe()

    async def collect_events(
        self, timeout_ms: int | None = None
    ) -> list[dict[str, Any]]:
        """Pi parity: ``rpc-client.ts:collectEvents``."""

        timeout_s = (timeout_ms or self.DEFAULT_WAIT_FOR_IDLE_MS) / 1000.0
        collected: list[dict[str, Any]] = []
        done = asyncio.get_running_loop().create_future()

        def _listener(event: dict[str, Any]) -> None:
            collected.append(event)
            if event.get("type") == "agent_end" and not done.done():
                done.set_result(None)

        unsubscribe = self.on_event(_listener)
        try:
            await asyncio.wait_for(done, timeout=timeout_s)
        finally:
            unsubscribe()
        return collected

    async def prompt_and_wait(
        self,
        message: str,
        images: list[dict[str, Any]] | None = None,
        timeout_ms: int | None = None,
    ) -> list[dict[str, Any]]:
        """Pi parity: ``rpc-client.ts:promptAndWait``."""

        # Wire the listener BEFORE sending so we don't miss the
        # ``agent_end`` that fires synchronously in fast-stub scenarios.
        timeout_s = (timeout_ms or self.DEFAULT_WAIT_FOR_IDLE_MS) / 1000.0
        collected: list[dict[str, Any]] = []
        done = asyncio.get_running_loop().create_future()

        def _listener(event: dict[str, Any]) -> None:
            collected.append(event)
            if event.get("type") == "agent_end" and not done.done():
                done.set_result(None)

        unsubscribe = self.on_event(_listener)
        try:
            await self.prompt(message, images)
            await asyncio.wait_for(done, timeout=timeout_s)
        finally:
            unsubscribe()
        return collected

    # === Internals ============================================================

    def _build_argv(self) -> list[str]:
        """Pi parity: ``rpc-client.ts:62-72`` argv construction.

        Default: ``python -m aelix --mode rpc``. ``cli_path`` overrides the
        default invocation (e.g. for a custom shim or a one-off stub).
        """

        opts = self._options
        cli_path = opts.cli_path
        if cli_path is not None:
            argv: list[str] = [sys.executable, cli_path, "--mode", "rpc"]
        else:
            argv = [sys.executable, "-m", "aelix", "--mode", "rpc"]
        if opts.provider:
            argv.extend(["--provider", opts.provider])
        if opts.model:
            argv.extend(["--model", opts.model])
        argv.extend(opts.args)
        return argv

    def _build_env(self) -> dict[str, str]:
        env = dict(os.environ)
        env.update(self._options.env)
        return env

    async def _drain_stderr(self, stream: asyncio.StreamReader) -> None:
        """Continuously drain the child's stderr into the capture buffer.

        W4 M4 — buffer is capped at :attr:`STDERR_MAX_BYTES` (10 MB).
        Excess bytes are evicted from the FRONT (FIFO) so the most-recent
        diagnostics survive for ``get_stderr()`` callers.
        """

        while True:
            chunk = await stream.read(4096)
            if not chunk:
                return
            self._stderr_buffer.extend(chunk)
            if len(self._stderr_buffer) > self.STDERR_MAX_BYTES:
                overflow = len(self._stderr_buffer) - self.STDERR_MAX_BYTES
                del self._stderr_buffer[:overflow]

    def _handle_stdout_line(self, line: str) -> None:
        """Pi parity: ``rpc-client.ts:handleLine`` — response vs. event split."""

        if not line.strip():
            return
        try:
            payload = json.loads(line)
        except (json.JSONDecodeError, ValueError):
            # Pi swallows non-JSON lines silently.
            return
        if not isinstance(payload, dict):
            return
        if payload.get("type") == "response":
            request_id = payload.get("id")
            if isinstance(request_id, str) and request_id in self._pending_requests:
                try:
                    response = parse_rpc_response(payload)
                except ValueError:
                    return
                future = self._pending_requests.pop(request_id)
                if not future.done():
                    future.set_result(response)
                return
            # W4 m8 — response arrived AFTER the originating request was
            # timed-out + popped. Log to stderr before falling through to
            # event listeners so operators can correlate the drift.
            if isinstance(request_id, str):
                print(
                    f"[rpc-client] stale response id={request_id!r} "
                    f"command={payload.get('command')!r} (request already "
                    "completed/timed out)",
                    file=sys.stderr,
                    flush=True,
                )
            # Response without a tracked id — surface to event listeners
            # so callers can see protocol drift.
        for listener in list(self._event_listeners):
            # Listener errors are isolated (Pi parity — TS try/catch would
            # have prevented one listener from breaking the others).
            with contextlib.suppress(Exception):  # noqa: BLE001
                listener(payload)

    async def _send(
        self, command: dict[str, Any]
    ) -> RpcSuccessResponse | RpcErrorResponse:
        """Send a command and await the correlated response.

        Pi parity: ``rpc-client.ts:323-343``. Drops ``None`` values from
        the payload so the wire shape matches Pi's TS omits-undefined behavior.
        """

        if self._proc is None or self._proc.stdin is None:
            raise RuntimeError("RpcClient not started")

        request_id = f"req_{next(self._next_id)}"
        payload: dict[str, Any] = {"id": request_id}
        for key, value in command.items():
            if value is None:
                continue
            payload[key] = value

        future: asyncio.Future[
            RpcSuccessResponse | RpcErrorResponse
        ] = asyncio.get_running_loop().create_future()
        self._pending_requests[request_id] = future

        line = serialize_json_line(payload).encode("utf-8")
        self._proc.stdin.write(line)
        try:
            await self._proc.stdin.drain()
        except (BrokenPipeError, ConnectionResetError) as exc:
            self._pending_requests.pop(request_id, None)
            raise RuntimeError(f"RPC server closed stdin: {exc}") from exc

        try:
            response = await asyncio.wait_for(
                future, timeout=self.DEFAULT_SEND_TIMEOUT_MS / 1000.0
            )
        except TimeoutError:
            self._pending_requests.pop(request_id, None)
            stderr_snapshot = self.get_stderr()
            raise TimeoutError(
                f"Timeout waiting for response to {command.get('type')!r}. "
                f"Stderr: {stderr_snapshot}"
            ) from None
        return response

    def _unwrap(
        self, response: RpcSuccessResponse | RpcErrorResponse
    ) -> Any:
        """Pi parity: ``rpc-client.ts:getData`` — raise on error, return data otherwise."""

        if isinstance(response, RpcErrorResponse):
            raise RpcClientError(response.command, response.error)
        return response.data


