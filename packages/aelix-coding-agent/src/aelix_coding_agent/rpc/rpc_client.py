"""Pi parity: ``packages/coding-agent/src/modes/rpc/rpc-client.ts`` (343 LOC).

:class:`RpcClient` spawns the Aelix CLI as a subprocess with ``--mode rpc``,
wraps stdin/stdout with JSONL framing, correlates responses by ``id``, and
broadcasts session events to listeners.

Pi parity invariants:

- 100 ms startup grace before failing if the child exited (Pi
  ``rpc-client.ts:81``).
- 30 s send timeout per command (Pi ``rpc-client.ts:332``).
- 60 s ``waitForIdle`` default (Pi ``rpc-client.ts:262``).
- SIGTERM → wait 1 s → SIGKILL (Pi ``rpc-client.ts:107-122``).
- ``id`` generated by monotonic counter (Pi ``requestId``).
- ``pending_requests: dict[id, Future]`` correlation map.

The Sprint 6d Python client ports the **full 29-method command surface** —
deferred commands receive the server's :class:`RpcErrorResponse` and raise
:class:`RpcClientError` so callers see Pi-shape failure semantics today.
"""

